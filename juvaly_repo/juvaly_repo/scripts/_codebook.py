"""共用的標註標準（Codebook），給 validate 與 label_batch 共用，確保標準一致"""
CODEBOOK = """你是嚴謹的美妝評論標註員。為評論標註三個欄位：

【sentiment】只填一個：正面 / 中性 / 負面 / 非評論
- 正面：明確稱讚、推薦、會回購、表達滿意。
- 中性：平淡描述、客套、無功無過、有好有壞。客套讚美算中性不算正面。
- 負面：明確抱怨、失望、嫌棄（委婉的如「我完全無法」「好沒驚喜」也算）。
- 非評論：整則是官方文案/規格說明、無個人使用感受時才用。含個人經驗即為真實評論。

【pain_points】明確抱怨的點，分號分隔，無則空字串。選項：
質地黏膩/吸收差/香味不佳/效果無感/致痘致敏/包裝設計/價格偏高/用量大/需搭配其他產品/
成分極簡但效果不足/精油或天然香味接受度低/為理念或包裝買單但實際普通/永續包裝犧牲使用體驗/認同品牌但不會回購/其他

【highlights】明確稱讚的點，規則同上。選項：
輕盈好吸收/保濕有感/溫和不刺激/無香精/抗老修護有感/包裝質感/香味宜人/成分純淨天然/使用儀式感佳/其他

鐵則：
1.「不黏膩」「不油膩」是亮點(輕盈好吸收)，絕不可標成質地黏膩。
2. 情感看整體語氣，不是看單一關鍵詞；客套讚美不等於真實正面。
3. 認同品牌理念 ≠ 對產品滿意，要區分。
4. 邀稿/公關品試用文若整段抄官方文案，標非評論。"""
